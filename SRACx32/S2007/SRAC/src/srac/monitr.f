C             MONITR              LEVEL=1        DATE=81.11.14
      SUBROUTINE MONITR (BAL)
C
      COMMON /TW1C/ DD(1),LIM1,IA(210)
      COMMON /WORK/AAA(1),LIM2,AA(130)
      DIMENSION D(212),A(132)
      EQUIVALENCE (D(1),DD(1)),(AAA(1),A(1))
      EQUIVALENCE (D(107),IGCDMP),(D(157),NOUT)
C
      EQUIVALENCE (IA(26),IITL),(IA(37),EV),(IA(40),XLAL),(IA(41),XLAH),
     &(IA(42),XLAX),(IA(43),EPSO),(IA(44),EPSI),(IA(45),EPSR),
     &(IA(46),EPSX),(IA(48),NORM),(IA(49),POD),(IA(50),BHGT),
     &(IA(62),IGP),( A(23),ICONV),( A(25),E3),( A(26),EVS),
     &( A(27),IITOT),( A(28),ALA),( A(29),TIN),( A(32),OITNO),
     &( A(50),TIMACC)
C
      DIMENSION BAL(1)
C
      INTEGER OITNO
      REAL NORM
C
      IF (IGCDMP.NE.0) GO TO 100
      CALL SECOND (TIME)
      TIME=(TIME-TIN+TIMACC)/60.0
      IF (OITNO.EQ.0) IITOT=0
      IF (OITNO.NE.0) GO TO 110
C
C     PRINT CONVERGENCE INFORMATION
C
  100 WRITE (NOUT,120)EPSO,XLAX,EPSI,XLAH,EPSR,XLAL,EPSX,POD,NORM,BHGT
      WRITE (NOUT,130)IITL
      IF (IGCDMP.EQ.0) GO TO 110
      WRITE (NOUT,160)
      RETURN
  110 CONTINUE
C
C     PRINT MONITOR HEADING
C
      IF ((OITNO.EQ.0).OR.(ICONV.EQ.1)) WRITE (NOUT,140)
C
C     PRINT MONITOR LINE
C
      WRITE (NOUT,150)TIME,OITNO,IITOT,BAL(IGP),EV,EVS,ALA,E3
      OITNO=OITNO+1
      RETURN
C
C
  120 FORMAT (1H0/' EPSO ',1PE10.2,6X,'XLAX ',1PE10.2/' EPSI ',1PE10.2,6
     &X,'XLAH ',1PE10.2/' EPSR ',1PE10.2,6X,'XLAL ',1PE10.2/' EPSX ',1PE
     &10.2,6X,'POD  ',1PE10.2/' NORM ',1PE10.2,6X,'BHGT ',1PE10.2/)
  130 FORMAT (' IITL ',I4)
  140 FORMAT(////' TIME IN',4X,'OUTER',7X,'INNER',7X,'NEUTRON',  7X,
     &'EIGENVALUE',5X,'EIGENVALUE',7X,'LAMBDA',8X,'REBALANCE'/   1H ,
     &'MINUTES ITARATIONS',2X,'ITERATIONS',5X,'BALANCE',24X,'SLOPE',23X,
     &'CONVERGENCE '/20X,'TOTAL BY GROUP ')
  150 FORMAT (1X,1PE9.2,I6,2X,I6,3X,7X,1P5E15.8)
  160 FORMAT (1H0///)
      END
