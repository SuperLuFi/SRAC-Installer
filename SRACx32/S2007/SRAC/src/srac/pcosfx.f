C
C     PRINT OUT OF MEMORY LOCATION FOR PEACO-ROUTINE
C
      SUBROUTINE  PCOSFX(NOUT)
C
      COMMON /PCOSF1/ KK(86)
      COMMON /PCOSF2/ JJ(8)
      COMMON /PCOSF3/ MM(4)
C
      WRITE(NOUT,10) KK
      WRITE(NOUT,20) JJ
      WRITE(NOUT,30) MM
C
   10 FORMAT(///1H ,5X,'* ADDRESS OF ARGUMENT.(PEACO) *',
     +//1H ,10X,')SUFX1>'//(1H ,5X,20I6/))
   20 FORMAT(//1H ,10X,')SUFX2>'//(1H ,5X,20I6/))
   30 FORMAT(//1H ,10X,')SUFX3>'//(1H ,5X,20I6/))
C
      RETURN
      END
