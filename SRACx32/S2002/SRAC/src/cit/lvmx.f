      DO 110 I = 1,6
      IF (NX(I)) 111,111,109
  109 NREGI = NREGI+1
C
      IF( NREGI . GE . RGX) THEN
      WRITE(6,'(1X,A)')
     &' ** MESSAGE FORM NREGI IN SUBROUTINE LVMX. **'
      WRITE(6,'(1X,A)')
     &'INSUFFICIENT REGION AREA NUMBER FOR RGX.'
      WRITE(6,'(1X,A,I5)')
     &'CURRENT NUMBER FOR RGX IS ',RGX
      WRITE(6,'(1X,A)')
     &'COULD YOU PLEASE CHECK YOUR NUMBER IN INCLUDE FILE IMMEDIATERY.'
      WRITE(6,'(1X,A)')
     &'I AM WILLING TO YOUR QUICK RESPONCE. THANK YOU.'
      STOP
      ENDIF
C
      MSHI(NREGI) = NX(I)
      XSHI(NREGI) = TX(I)
  110 CONTINUE
      GO TO 108
  111 IF (NDIM-2) 116,116,112
  112 NREGKB = 0
  113 READ(IOIN,1000)(NX(I),TX(I),I=1,6)
      DO 115 I = 1,6
      IF (NX(I)) 116,116,114
  114 NREGKB = NREGKB+1
C
      IF( NREGKB. GE . RGX) THEN
      WRITE(6,'(1X,A)')
     &' ** MESSAGE FORM NREGKB IN SUBROUTINE LVMX. **'
      WRITE(6,'(1X,A)')
     &'INSUFFICIENT REGION AREA NUMBER FOR RGX.'
