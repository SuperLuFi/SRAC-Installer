      WRITE(6,'(1X,A)')
     &'I AM WILLING TO YOUR QUICK RESPONCE. THANK YOU.'
      STOP
      ENDIF
C
      MSHKB(NREGKB) = NX(I)
      XSHKB(NREGKB) = TX(I)
  115 CONTINUE
      GO TO 113
  116 LMAX = NREGJ*NREGI*NREGKB
      RETURN
 1000 FORMAT(6(I3,E9.0))
      END
