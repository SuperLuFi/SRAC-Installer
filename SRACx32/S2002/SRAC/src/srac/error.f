C             ERROR               LEVEL=1        DATE=81.11.14
      SUBROUTINE ERROR (I,H,NARG)
C
C     PRINT ERROR MESSAGES
C
      COMMON /TW1C/ DD(1),LIM1,IAA(210)
      DIMENSION D(212)
      EQUIVALENCE (D(1),DD(1))
      EQUIVALENCE (D(142),NERROR),(D(151),NEXTER),(D(157),NOU),
     &(D(160),NDUMP2)
C
      DIMENSION H(1)
C
CSASA REAL*8 H
      CHARACTER*8 H
C
      KEY=0
      NOUT=6
      GO TO 50
   30 NOUT=NOU
      KEY=1
   50 IF (I.EQ.3) GO TO 100
      IF (I.NE.5) GO TO 130
  100 WRITE (NOUT,150)
      IF (I.EQ.5) GO TO 110
      WRITE (NOUT,170)(H(J),J=1,NARG)
      GO TO 120
C     FOUR WORDS OF EIGTH CHARACTERS EACH PLUS INTEGER I2 PRINT
  110 WRITE (NOUT,190)(H(J),J=1,NARG),NDUMP2
  120 WRITE (NOUT,150)
      WRITE (NOUT,180)
      RETURN
  130 WRITE (NOUT,140)
      WRITE (NOUT,150)
      WRITE (NOUT,160)(H(J),J=1,NARG)
      WRITE (NOUT,150)
      WRITE (NOUT,140)
CKSK  IF (I.EQ.1) CALL EXIT
      IF (I.EQ.1) STOP
      IF (I.EQ.2) NERROR=2
      IF (I.EQ.4) NEXTER=1
      IF(KEY.EQ.0) GO TO 30
      RETURN
C
C
  140 FORMAT (1H0////)
  150 FORMAT (1H0,119('*'))
  160 FORMAT ('0***ERROR** ',18A6)
  170 FORMAT (1H0,18A6)
  180 FORMAT (1H0)
  190 FORMAT (1H0,4A8,I2)
      END
