CKDWT --140 ***CITATION*** PRINT REL. POW. DENS. TRAVERSES/ CF-OUTC
C
      SUBROUTINE KDWT(P2E,UTILE,NRGNE, PVOL,IPRIN, IVX,JVX,KBVX,KVX,
     & LVX,JIVX,SIG,NCOMP,MVX)
C
CDEL  INTEGER RGX , MSX , ZNEX , ZDX , WZX
CDEL  PARAMETER ( RGX=100, MSX=211, ZDX=200, ZNEX=1000, WZX=100 )
      INCLUDE  'CITPMINC'
C
      COMMON/ALSUB/BLSUB(30),TITL1(18),TITL2(18),IMAX,JMAX,KBMAX,KMAX,
     & LMAX,MMAX, NMAX,IMXP1,JMXP1,KBMXP1,NSETMX,NTO,MM1VX,KM1VX,IOIN,
     & IOUT,IOSIG,IOFLX,IO1,IO2,IO3,IO4,IO7,NER(100), IX(200),INNO(100),
     &  NGC(24),IEDG(24),ITMX(24),TIMX(6), GLIM(6),NDPL(24),IEDP1(24),
     & IEDP2(24),IEDP3(24), DPLH(6),NUAC(24),EPI(6),XMIS(6),NSRH(24),
     & XSRH1(6), XTR1(WZX),XTR2(WZX),NXTR1(WZX),NXTR2(WZX),SPARE(200),
     & IXPUT(200),XPUT(200)
      COMMON/AMESH/BMESH(30),NREGI,NREGJ,NREGKB,XSHI(RGX),XSHJ(RGX),
     & XSHKB(RGX), MSHI(RGX),MSHJ(RGX),MSHKB(RGX),Y(MSX),YY(MSX), X(MSX)
     &  ,XX(MSX),Z(MSX),ZZ(MSX), ZONVOL(ZNEX),AVZPD(ZNEX),PDI(MSX),
     & PDJ(MSX) , PDK(MSX)
C
      DIMENSION P2E(JIVX ,KBVX,KVX),UTILE(JVX,IVX,KBVX ), NRGNE(JVX,IVX,
     & KBVX)
      DIMENSION PVOL(LVX)
      DIMENSION SIG(KVX,MVX,10),NCOMP(LVX)
C
CKSK  T2 = PDI(211)
      T2 = PDI(MSX)
      IAX = IMAX
      IF (IMAX.GT.200) IAX = 200
      DO 100 N = 1,IAX
      XTR1(N) = PDI(N)
  100 CONTINUE
      DO 105 KB = 1,KBMAX
  101 DO 104 I = 1,IMAX
      NN1= (I-1)*JVX
      DO 103 J = 1,JMAX
      N1= NN1 + J
      L = NRGNE(J,I,KB)
      M = NCOMP(L)
      UTILE(J,I,KB ) = 0.0
      DO 102 K = 1,KMAX
      UTILE(J,I,KB ) = UTILE(J,I,KB )+P2E(N1 ,KB,K)*SIG(K,M,7)
  102 CONTINUE
  103 CONTINUE
  104 CONTINUE
  105 CONTINUE
      IF (IPRIN.GT.0) GO TO 120
      CALL RQED(IX(113),IND)
      IF (IND.NE.0) GO TO 120
  106 T1 = 0.0
      DO 110 KB = 1,KBMAX
      DO 109 I = 1,IMAX
      DO 108 J = 1,JMAX
      IF (UTILE(J,I,KB)-T1) 108,107,107
  107 II = I
      KKB = KB
      JJ = J
      T1 = UTILE(J,I,KB )
  108 CONTINUE
  109 CONTINUE
  110 CONTINUE
      XT1 = 0.0
      XV1 = 0.0
      YT1 = 0.0
      YV1 = 0.0
      ZT1 = 0.0
      ZV1 = 0.0
      DO 112 J = 1,JMAX
      L = NRGNE(J,II,KKB)
      IF (UTILE(J,II,KKB)) 111,112,111
  111 XT1 = XT1+UTILE(J,II,KKB )*PVOL(L)
      XV1 = XV1+PVOL(L)
  112 CONTINUE
      DO 114 I = 1,IMAX
      L = NRGNE(JJ,I,KKB)
      IF (UTILE(JJ,I,KKB)) 113,114,113
  113 YT1 = YT1+UTILE(JJ,I,KKB )*PVOL(L)
      YV1 = YV1+PVOL(L)
  114 CONTINUE
      DO 116 KB = 1,KBMAX
      L = NRGNE(JJ,II,KB)
      IF (UTILE(JJ,II,KB)) 115,116,115
  115 ZT1 = ZT1+UTILE(JJ,II,KB )*PVOL(L)
      ZV1 = ZV1+PVOL(L)
  116 CONTINUE
      XB1 = XT1/XV1
      YB1 = YT1/YV1
      ZB1 = ZT1/ZV1
      DO 117 J = 1,JMAX
      PDJ(J) = UTILE(J,II,KKB )/XB1
  117 CONTINUE
      DO 118 I = 1,IMAX
      PDI(I) = UTILE(JJ,I,KKB )/YB1
  118 CONTINUE
      DO 119 KB = 1,KBMAX
      PDK (KB) = UTILE(JJ,II,KB )/ZB1
  119 CONTINUE
      WRITE(IOUT,1000)II,JJ,KKB,UTILE(JJ,II,KKB)
      WRITE(IOUT,1001)II,XB1,(PDJ(J),J=1,JMAX)
      WRITE(IOUT,1002)JJ,YB1,(PDI(I),I=1,IMAX)
      WRITE(IOUT,1003)KKB,ZB1,(PDK (KB),KB=1,KBMAX)
  120 CONTINUE
CKSK  PDI(211) = T2
      PDI(MSX) = T2
      DO 121 N = 1,IAX
      PDI(N) = XTR1(N)
  121 CONTINUE
      RETURN
 1000 FORMAT(1H0/'0THE MAXIMUM POWER DENSITY','(WATTS/CC) AT ROW',I4,
     &  '  AND COLUMN',I4,'  AND LINE',I4,'  IS ',1PE14.6)
 1001 FORMAT(1H0,'THE AVERAGE POWER DENSITY ALONG ROW',I4,
     & '  IS',1PE14.6,'  AND THE RELATIVE POWER DENSITIES',
     & '(FRACTION OF AVERAGE) ARE'/(1H ,9E14.6))
 1002 FORMAT(1H0,'THE AVERAGE POWER DENSITY DOWN COLUMN',I4,
     & '  IS',1PE14.6,'  AND THE RELATIVE POWER DENSITIES',' ARE'/(
     &  1H ,9E14.6))
 1003 FORMAT(1H0,'THE AVERAGE POWER DENSITY ALONG LINE',I4,
     & '  IS',1PE14.6,'  AND THE RELATIVE POWER DENSITIES ARE'/(
     &  1H ,9E14.6))
      END
