      SUBROUTINE IPRTX (HOL1,HOL2,NX,NY,MESHX)
      DIMENSION MESHX(NX,1)
      COMMON /MAINC/ DUM1(64),NOUT2,IT0,DUM2(36),TITLE(18),DUM3(380)
      CHARACTER*4 IBL(2),IAS(2),HOL1,HOL2,IBLR,IBLL
      DATA IBL/'    ','   *'/, IAS/'****','****'/,
     1    IBLR/'    '/,IBLL/'  * '/
      I0=1
   10 I1=MIN0(NX,I0+14)
      N5=5*(I1-I0+1)
      LINE=60
      DO 200 J=1,NY
      IF(LINE.LT.60) GOTO 150
      WRITE(NOUT2,8000) TITLE,HOL1,HOL2
 8000 FORMAT(1H1///5X,5H**** ,18A4,5X,5H**** ,2A4,12H NUMBER **** )
 8001 FORMAT(1H ,9H COL.  **,15(2H *,I2,4H * *))
 8002 FORMAT(1H ,2A4,1H*,30A4)
 8003 FORMAT(1H ,2H *,I2,5H * **,15(A2,I3,A3))
 8005 FORMAT(10H        **,15(A2,3A1,A3))
      WRITE(NOUT2,8002) (IBL(1),IBL(2),I=I0,I1+1)
      WRITE(NOUT2,8001) (I,I=I0,I1)
      WRITE(NOUT2,8002) (IBL(1),IBL(2),I=I0,I1+1)
      WRITE(NOUT2,8002) (IAS(1),IAS(2),I=I0,I1+1)
      WRITE(NOUT2,8002) (IAS(1),IAS(2),I=I0,I1+1)
      LINE=10
  150 CONTINUE
      WRITE(NOUT2,8002) (IBL(1),IBL(2),I=I0,I1+1)
C     WRITE(NOUT2,9000) I0,I1,J,(MESHX(I,J),I=1,NX)
C9000 FORMAT(1H ,10X,3HI0=,I5,2X,3HI1=,I5,2X,2HJ=,I5,
C    1       (/1H ,10X,11HMESHX(I,J)=,I5))
      WRITE(NOUT2,8003) J,(IBLR,MESHX(I,J),IBLL,I=I0,I1)
      WRITE(NOUT2,8002) (IBL(1),IBL(2),I=I0,I1+1)
      WRITE(NOUT2,8002) (IAS(1),IAS(2),I=I0,I1+1)
      LINE=LINE+4
  200 CONTINUE
      IF(I1.GE.NX) RETURN
      I0=I1+1
      GO TO 10
      END
