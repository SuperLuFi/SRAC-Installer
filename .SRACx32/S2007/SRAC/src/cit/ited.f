      WRITE(IOUT,1002)NIT,VRG1,BETTX,XT1,XT2,XMU3,V,EXFC1,XLAMDA,
     & SPARE(89)
      GO TO 105
  104 WRITE(IOUT,1003)NIT,VRG1,BETTX,XT1,XT2,XMU3,XLAMDA ,SPARE(89)
  105 CONTINUE
      RETURN
