C***************************SEARCH OPTIONS******************************
      IF ((IX(5).EQ.0).OR.(IX(5).GE.2)) GO TO 101
      IF (IX(5).EQ.(-5)) GO TO 103
      IF (IX(32).EQ.0) GO TO 100
      V = EXFC1*(1.0-1.0/XT3)
      WRITE(IOUT,1001)NIT,VRG1,BETTX,XT1,XT2,XMU3,V,EXFC1,XKEF1,XLAMDA
