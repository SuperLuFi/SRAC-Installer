CPDWT --139 ***CITATION*** PRINT REL. POW. DENS. TRAVERSES/ CF-OUTC
C
      SUBROUTINE PDWT(P2,UTIL,NRGN, PVOL,IPRIN, IVX,JVX,KVX,LVX, SIG,
     & NCOMP,MVX)
C
CDEL  INTEGER RGX , MSX , ZNEX , ZDX , WZX
CDEL  PARAMETER ( RGX=100, MSX=211, ZDX=200, ZNEX=1000, WZX=100 )
      INCLUDE  'CITPMINC'
C
      REAL*8 P2
C
      COMMON/ALSUB/BLSUB(30),TITL1(18),TITL2(18),IMAX,JMAX,KBMAX,KMAX,
     & LMAX,MMAX, NMAX,IMXP1,JMXP1,KBMXP1,NSETMX,NTO,MM1VX,KM1VX,IOIN,
     & IOUT,IOSIG,IOFLX,IO1,IO2,IO3,IO4,IO7,NER(100), IX(200),INNO(100),
     &  NGC(24),IEDG(24),ITMX(24),TIMX(6), GLIM(6),NDPL(24),IEDP1(24),
     & IEDP2(24),IEDP3(24), DPLH(6),NUAC(24),EPI(6),XMIS(6),NSRH(24),
     & XSRH1(6), XTR1(WZX),XTR2(WZX),NXTR1(WZX),NXTR2(WZX),SPARE(200),
     & IXPUT(200),XPUT(200)
      COMMON/AMESH/BMESH(30),NREGI,NREGJ,NREGKB,XSHI(RGX),XSHJ(RGX),
     & XSHKB(RGX), MSHI(RGX),MSHJ(RGX),MSHKB(RGX),Y(MSX),YY(MSX), X(MSX)
     &  ,XX(MSX),Z(MSX),ZZ(MSX), ZONVOL(ZNEX),AVZPD(ZNEX),PDI(MSX),
     & PDJ(MSX) , PDK(MSX)
C
      DIMENSION P2(JVX,IVX,KVX),UTIL(JVX,IVX ), NRGN(JVX, IVX)
      DIMENSION PVOL(LVX)
      DIMENSION SIG(KVX,MVX,10),NCOMP(LVX)
C
CM    T2 = PDI(211)
      T2 = PDI(MSX)
C----- CHANGE END
      IAX = IMAX
      IF (IMAX.GT.200) IAX = 200
      DO 100 N = 1,IAX
      XTR1(N) = PDI(N)
  100 CONTINUE
  101 DO 104 I = 1,IMAX
      DO 103 J = 1,JMAX
      L = NRGN(J,I)
      M = NCOMP(L)
      UTIL(J,I )=0.0
      DO 102 K = 1,KMAX
      UTIL(J,I ) = UTIL(J,I )+P2(J,I,K)*SIG(K,M,7)
  102 CONTINUE
  103 CONTINUE
  104 CONTINUE
      IF (IPRIN.GT.0) GO TO 115
      CALL RQED(IX(113),IND)
      IF (IND.NE.0) GO TO 115
  105 T1 = 0.0
      DO 108 I = 1,IMAX
      DO 107 J = 1,JMAX
      IF (UTIL(J,I)-T1) 107,106,106
  106 II = I
      JJ = J
      T1 = UTIL(J,I )
  107 CONTINUE
  108 CONTINUE
      XT1 = 0.0
      XV1 = 0.0
      YT1 = 0.0
      YV1 = 0.0
      DO 110 J = 1,JMAX
      L = NRGN(J,II)
      IF (UTIL(J,II)) 109,110,109
  109 XT1 = XT1+UTIL(J,II )*PVOL(L)
      XV1 = XV1+PVOL(L)
  110 CONTINUE
      DO 112 I = 1,IMAX
      L = NRGN(JJ,I)
      IF (UTIL(JJ,I)) 111,112,111
  111 YT1 = YT1+UTIL(JJ,I )*PVOL(L)
      YV1 = YV1+PVOL(L)
  112 CONTINUE
      XB1 = XT1/XV1
      YB1 = YT1/YV1
      DO 113 J = 1,JMAX
      PDJ(J) = UTIL(J,II )/XB1
  113 CONTINUE
      DO 114 I = 1,IMAX
      PDI(I) = UTIL(JJ,I )/YB1
  114 CONTINUE
      WRITE(IOUT,1000)II,JJ,UTIL(JJ,II)
      WRITE(IOUT,1001)II,XB1,(PDJ(J),J=1,JMAX)
      WRITE(IOUT,1002)JJ,YB1,(PDI(I),I=1,IMAX)
  115 CONTINUE
CM    PDI(211) = T2
      PDI(MSX) = T2
      DO 116 N = 1,IAX
      PDI(N) = XTR1(N)
  116 CONTINUE
      RETURN
 1000 FORMAT(1H0/'0THE MAXIMUM POWER DENSITY (WATTS/CC) AT ROW',I4,
     &  2H  ,'AND COLUMN',I4,'  IS ',1PE14.6)
 1001 FORMAT('0THE AVERAGE POWER DENSITY ALONG ROW',I4,
     & '  IS',1PE14.6,'  AND THE RELATIVE POWER DENSITIES',
     & '(FRACTION OF AVERAGE) ARE'/(1H ,9E14.6))
 1002 FORMAT('0THE AVERAGE POWER DENSITY DOWN COLUMN',I4,
     & '  IS',1PE14.6,'  AND THE RELATIVE POWER DENSITIES ARE'/(
     &  1H ,9E14.6))
      END
