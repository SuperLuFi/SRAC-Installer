      SUBROUTINE REAPRT(MX,MY,MZ,IGO,R,JERR,IEND,NOUT2,IFILTR,IFILT1)
C
C     PRINT OUT CALCULATED REACTION RATE
C
      CHARACTER * 12 TITL(2)
      DIMENSION R(2)
      DATA TITL/'NON-FILTERED','FILTERED    '/
C
      IF (IEND.EQ.0) RETURN
C
      GO TO (100,200,300),IGO
  100 CONTINUE
C----- FISSION
      IF(JERR.NE.0) GO TO 110
      WRITE(NOUT2,6000) MX,MY,MZ,R(1)
      IF (IFILTR.EQ.1) WRITE(NOUT2,6010) TITL(IFILT1+1)
      GO TO 1000
  110 WRITE(NOUT2,6100) MX,MY,MZ
      GO TO 1000
  200 CONTINUE
C----- FISSION
      IF(JERR.NE.0) GO TO 210
      WRITE(NOUT2,6000) MX,MY,MZ,R(2)
      IF (IFILTR.EQ.1) WRITE(NOUT2,6010) TITL(IFILT1+1)
      GO TO 1000
  210 WRITE(NOUT2,6200) MX,MY,MZ
      GO TO 1000
  300 CONTINUE
C----- FISSION & CAPTURE
      IF(JERR.NE.0) GO TO 310
      WRITE(NOUT2,6000) MX,MY,MZ,R(1),R(2)
      IF (IFILTR.EQ.1) WRITE(NOUT2,6020) TITL(IFILT1+1)
      GO TO 1000
  310 WRITE(NOUT2,6300) MX,MY,MZ
 1000 CONTINUE
      RETURN
 6000 FORMAT(1H ,4X,3(7X,I3),6X,1P2E17.5)
 6010 FORMAT(1H+,58X,'--- ',A12)
 6020 FORMAT(1H+,74X,'--- ',A12)
 6100 FORMAT(1H ,4X,3(7X,I3),11X,'* MESH OVER *')
 6200 FORMAT(1H ,4X,3(7X,I3),11X,'* MESH OVER *')
 6300 FORMAT(1H ,4X,3(7X,I3),11X,'* MESH OVER *',4X,'* MESH OVER *')
      END
