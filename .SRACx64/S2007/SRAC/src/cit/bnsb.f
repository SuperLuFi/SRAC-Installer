CBNSB --055 ***CITATION*** PUT BND. COND. CNSTS. IN A(N)/ CF-IPTM
C
      SUBROUTINE BNSB(BBND,BND,KVX,IRV)
C
CDEL  INTEGER RGX , MSX , ZNEX , ZDX , WZX
CDEL  PARAMETER ( RGX=100, MSX=211, ZDX=200, ZNEX=1000, WZX=100 )
      INCLUDE  'CITPMINC'
C
      COMMON/ALSUB/BLSUB(30),TITL1(18),TITL2(18),IMAX,JMAX,KBMAX,KMAX,
     & LMAX,MMAX, NMAX,IMXP1,JMXP1,KBMXP1,NSETMX,NTO,MM1VX,KM1VX,IOIN,
     & IOUT,IOSIG,IOFLX,IO1,IO2,IO3,IO4,IO7,NER(100), IX(200),INNO(100),
     &  NGC(24),IEDG(24),ITMX(24),TIMX(6), GLIM(6),NDPL(24),IEDP1(24),
     & IEDP2(24),IEDP3(24), DPLH(6),NUAC(24),EPI(6),XMIS(6),NSRH(24),
     & XSRH1(6), XTR1(WZX),XTR2(WZX),NXTR1(WZX),NXTR2(WZX),SPARE(200),
     & IXPUT(200),XPUT(200)
C
      DIMENSION BBND(KVX),BND(6,KVX)
C
      IO19 = IX(86)
      REWIND IO19
      IF (IRV.NE.1) GO TO 100
      WRITE(IO19) ((BND(I,K),I=1,6),K=1,KVX),(BBND(K),K=1,KVX)
      END FILE IO19
      GO TO 121
  100 IF (INNO(3).EQ.3) GO TO 101
      READ(IO19) ((BND(I,K),I=1,6),K=1,KVX),(BBND(K),K=1,KVX)
      GO TO 121
  101 DO 103 K=1,KVX
      DO 102 I=1,6
      BND(I,K) = 0.0
  102 CONTINUE
  103 CONTINUE
      IF (XMIS(1).GE.0.0.AND.XMIS(2).GE.0.0) GO TO 107
  104 READ(IO19,END=107) I,(BBND(K),K=1,KVX)
      IF (I.GT.6) GO TO 106
      IF (I.LT.1) GO TO 120
      DO 105 K=1,KVX
      BND(I,K) = BBND(K)
  105 CONTINUE
      GO TO 104
  106 IF (I.NE.7) GO TO 120
  107 CONTINUE
      IF (XMIS(1)) 113,108,109
  108 T1 = 0.4692
      GO TO 110
  109 T1 = XMIS(1)
  110 DO 112 I=11,16
      II = I - 10
      IF (NUAC(I).NE.0) GO TO 112
      DO 111 K=1,KVX
      BND(II,K) = T1
  111 CONTINUE
  112 CONTINUE
  113 CONTINUE
      IF (NUAC(17).GT.0) GO TO 115
      DO 114 K=1,KVX
      BBND(K) = 0.0
  114 CONTINUE
      GO TO 121
  115 CONTINUE
      IF (XMIS(2)) 121,116,117
  116 T1 = 0.4692
      GO TO 118
  117 T1 = XMIS(2)
  118 DO 119 K=1,KMAX
      BBND(K) = T1
  119 CONTINUE
      GO TO 121
  120 CONTINUE
      NER(33) = 33
  121 CONTINUE
      REWIND IO19
      RETURN
      END
