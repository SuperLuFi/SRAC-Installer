CBKLE --039 ***CITATION*** READS INPUT SECTION 024/ CF-IPTM
C
      SUBROUTINE BKLE(SIG, MVX,KVX)
C
CDEL  INTEGER RGX , MSX , ZNEX , ZDX , WZX
CDEL  PARAMETER ( RGX=100, MSX=211, ZDX=200, ZNEX=1000, WZX=100 )
      INCLUDE  'CITPMINC'
C
      COMMON/ALSUB/BLSUB(30),TITL1(18),TITL2(18),IMAX,JMAX,KBMAX,KMAX,
     & LMAX,MMAX, NMAX,IMXP1,JMXP1,KBMXP1,NSETMX,NTO,MM1VX,KM1VX,IOIN,
     & IOUT,IOSIG,IOFLX,IO1,IO2,IO3,IO4,IO7,NER(100), IX(200),INNO(100),
     &  NGC(24),IEDG(24),ITMX(24),TIMX(6), GLIM(6),NDPL(24),IEDP1(24),
     & IEDP2(24),IEDP3(24), DPLH(6),NUAC(24),EPI(6),XMIS(6),NSRH(24),
     & XSRH1(6), XTR1(WZX),XTR2(WZX),NXTR1(WZX),NXTR2(WZX),SPARE(200),
     & IXPUT(200),XPUT(200)
C
      DIMENSION SIG(KVX,MVX,10)
C
  100 READ(IOIN,1000)IND,BUCK
      GO TO (101,104,107),IND
  101 DO 103 M=1,MMAX
      DO 102 K = 1,KMAX
      SIG(K,M,6) = BUCK
  102 CONTINUE
  103 CONTINUE
      GO TO 112
  104 READ(IOIN,1001) (SIG(K,1,10),K=1,KMAX)
      DO 106 M = 1,MMAX
      DO 105 K = 1,KMAX
      SIG(K,M,6) = SIG(K,1,10)
  105 CONTINUE
  106 CONTINUE
      GO TO 112
  107 READ(IOIN,1002)N1,N2
      IF (N2) 108,108,109
  108 N2 = N1
      IF (N1) 112,112,109
  109 READ(IOIN,1001) (SIG(K,1,10),K=1,KMAX)
      DO 111 M = N1,N2
      DO 110 K = 1,KMAX
      SIG(K,M,6) = SIG(K,1,10)
  110 CONTINUE
  111 CONTINUE
      GO TO 107
  112 RETURN
 1000 FORMAT(I3,E9.0)
 1001 FORMAT(6E12.0)
 1002 FORMAT(2I3)
      END
