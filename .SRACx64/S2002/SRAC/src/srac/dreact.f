      SUBROUTINE DREACT(IGMAXC,IREAC,IFILT,FG,SIGMA,FLUX,R,ISKIP)
C
C     CALCULATE DETECTOR REACTION RATE
C
      DIMENSION FG(IGMAXC),SIGMA(2,IGMAXC),FLUX(IGMAXC)
C
      ISKIP=0
      IF(FLUX(1).LT.-1.0E10) GO TO 2000
      R = 0.0
      DO 1000 I=1,IGMAXC
          IF(IREAC.NE.0) GO TO 100
          IF(IFILT.NE.0) GO TO 200
          R=R+SIGMA(1,I)*FLUX(I)
          GO TO 1000
  200     R=R+FG(I)*SIGMA(1,I)*FLUX(I)
          GO TO 1000
  100     CONTINUE
          IF(IFILT.NE.0) GO TO 300
          R=R+SIGMA(2,I)*FLUX(I)
          GO TO 1000
  300     R=R+FG(I)*SIGMA(2,I)*FLUX(I)
 1000 CONTINUE
      GO TO 3000
 2000 CONTINUE
      ISKIP=999
 3000 CONTINUE
      RETURN
      END
