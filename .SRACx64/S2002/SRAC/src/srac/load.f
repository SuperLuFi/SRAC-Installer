C             LOAD                LEVEL=1        DATE=81.11.14
      SUBROUTINE LOAD (H1,H2,B,IB,NC,ITYPI)
C
C     ITYPI -2/-1/0/1/2 FIXED TITLE /FIXED/FIDO TITLE/FLOAT/FLOAT TITLE
C
      COMMON /TW1C/ DD(1),LIM1,IAA(210)
      DIMENSION D(212)
      EQUIVALENCE (D(1),DD(1))
      EQUIVALENCE (D(157),NOUT)
C
      DIMENSION B(1), IB(1)
C
CSASA  REAL*8 H1,H2
       CHARACTER*8 H1,H2
C
      WRITE (NOUT,610)H1,H2,NC
C
C     SET TYPE AND PRINT INDICATORS
C
      ITYPE=0
      IF (ITYPI.GE.0) ITYPE=1
      IPRNT=0
      IF (IABS(ITYPI).EQ.2) IPRNT=1
      IF (ITYPI.EQ.0) IPRNT=1
  100 IF (ITYPI) 260,250,250
C
C     READ FLOATING POINT DATA
C
  250 CONTINUE
      CALL REAM(B,B,B,0,0,NC)
      GO TO 270
C
C     READ FIXED POINT DATA
C
  260  CONTINUE
      CALL REAM(IB,IB,IB,0,NC,0)
  270 CONTINUE
      L=NC
      IF (ITYPE.EQ.0) GO TO 470
      IF (IPRNT.NE.0) GO TO 500
      DO 450 I=1,L
      IF (B(I).NE.B(1)) GO TO 460
  450 CONTINUE
      WRITE (NOUT,820)B(1)
      GO TO 500
  460 WRITE (NOUT,640)(B(I),I=1,L)
      GO TO 500
  470 IF (IPRNT.NE.0) GO TO 500
      DO 480 I=1,L
      IF (IB(I).NE.IB(1)) GO TO 490
  480 CONTINUE
      WRITE (NOUT,830)IB(1)
      GO TO 500
  490 WRITE (NOUT,650)(IB(I),I=1,L)
  500 CONTINUE
      RETURN
  610 FORMAT('0INPUT ',2A6,I6   )
  630 FORMAT (6(I1,I2,I9))
  640 FORMAT (1H ,1P10E12.4)
  650 FORMAT (1H ,10I12)
  820 FORMAT (' ALL ENTRIES = ',1P1E12.4)
  830 FORMAT (' ALL ENTRIES = ',I12)
      END
